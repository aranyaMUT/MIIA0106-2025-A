#include <iostream>
using namespace std;
int main() {
	int a, b;
	cout << "Enter a: ";
	cin >> a;
	cout << "Enter b: ";
	cin >> b;

	cout << "A + B =" << a + b << endl;
	cout << "A - B =" << a - b << endl;
	cout << "A * B =" << a * b << endl;
	cout << "A / B =" << a / b << endl;
	cout << "A %% B =" << a % b << endl;

	return 0;
}